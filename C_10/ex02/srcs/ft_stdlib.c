int	ft_atoi(char *str)
{
	int				result;

	result = 0;
	while (*str >= '0' && *str <= '9')
	{
		result *= 10;
		result += *(str++) - '0';
	}
	if (*str == 0)
		return (result);
	return (-1);
}
