char	*ft_strcat(char *dest, char *src)
{
	char	*ret;

	ret = dest;
	while (*dest != '\x00')
		dest++;
	while (*src != '\x00')
	{
		*dest = *src;
		dest++;
		src++;
	}
	*dest = '\x00';
	return (ret);
}
