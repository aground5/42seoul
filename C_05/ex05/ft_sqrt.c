int	ft_sqrt(int nb)
{
	int	sqrt;

	sqrt = 0;
	while (sqrt < 46341)
	{
		if (sqrt * sqrt == nb)
			return (sqrt);
		else if (sqrt * sqrt > nb)
			return (0);
		sqrt++;
	}
	return (0);
}
