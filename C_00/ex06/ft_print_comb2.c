#include <unistd.h>

void	ft_putnum(int n)
{
	char	d1;
	char	d2;

	d1 = n % 10 + 0x30;
	n /= 10;
	d2 = n % 10 + 0x30;
	write(1, &d2, 1);
	write(1, &d1, 1);
}

void	ft_print_comb2(void)
{
	int	v1;
	int	v2;

	v1 = 0;
	while (v1 <= 99)
	{
		v2 = v1 + 1;
		while (v2 <= 99)
		{
			ft_putnum(v1);
			write(1, " ", 1);
			ft_putnum(v2);
			if (v1 != 98 || v2 != 99)
			{
				write(1, ", ", 2);
			}
			v2++;
		}
		v1++;
	}
}
