char	*ft_strlowcase(char *str)
{
	char	*ret;

	ret = str;
	while (*str != '\x00')
	{
		if (*str >= 'A' && *str <= 'Z')
		{
			*str = *str - 'A' + 'a';
		}
		str++;
	}
	return (ret);
}
