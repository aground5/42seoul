char	*ft_strupcase(char *str)
{
	char	*ret;

	ret = str;
	while (*str != '\x00')
	{
		if (*str >= 'a' && *str <= 'z')
		{
			*str = *str - 'a' + 'A';
		}
		str++;
	}
	return (ret);
}
