int	ft_putchar(int c)
{
	write(1, &c, sizeof(char));
	return (1);
}

int	ft_putstr(char *s)
{
	int	i;

	i = 0;
	while (s[i] != 0)
		i += ft_putchar(s[i]);
	return (i);
}